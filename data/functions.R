                                   "NA")))
  return(ride.dur)
}

# 3. obstacle UI
obstacle.uifun <- function(mx.dmg.extent = 0.2){
  # variable dmg chance
  # damage extent
  # mx.dmg.extent : max possible numeric amt by which to reduce bcond
  # default 0.2
  dmg.extent <- sample(seq(mx.dmg.extent, 0.01), 1) # possible amt to reduce bcond
  dmg.message <- ifelse(dmg.extent > 0.1, "heavy", "light")
  ui.msg <- dlg_message(paste0("Cancel your ride?",
                               " If `no`, your bike could sustain ", 
                               dmg.message, " damage..."), "yesno")$res
  if(ui.msg == "no"){
    # whether damaged (50% default)
    dmg.roll <- sample(c("damaged", "undamaged"), 1)
    obstacle.outcome <- sample(dmg.roll, 1)
    if(obstacle.outcome == "damaged"){
      bcond <- bcond - dmg.extent
    }
    ooutcome.msg <- paste0("Your bike sustained some damage. (bcond reduced by ", dmg.extent,
                           " to ", bcond, ").")
    dlg_message(ooutcome.msg, "ok")
    return(1) # continues ride
  } else if(ui.msg == "yes"){
    return(0)
  } else{
    return(NULL)
  }
}

ride.normal <- function(alabel = "ride mode: normal", msgperc,
                        framevector = fv.drive, 
                        ssint = 0.1, loops = 1){
  # this is code 
  # for normal ride seq animation
  grid.newpage()
  c = 1
  while(c <= loops){
    for(f in framevector){
      framewithlabel <- paste0(c(alabel, f, msgperc), 
                               collapse = "\n")
      grid.newpage()
      grid.text(framewithlabel)
      Sys.sleep(ssint)
    }
    c = c + 1
  }
}
ride.obstacle <- function(alabel = "ride mode: obstacle", 
                          msgperc, framevector1 = fv.idle, 
                          ssint = ssint, loops = 2){
  # sequences the obstacle encounter animation
  fv.obstacle <- ascii_obstacle_fv()
  grid.newpage()
  c = 1
  # grab obstacle data
  framevector1 <- rep(fv.idle, 2)
  framevector2 <- ascii_obstacle_fv()
  while(c <= loops){
    for(i in 1:length(framevector1)){
      grid.newpage()
      # print ride animation
      fs <- framevector1[i]
      frame1 <- paste0(c(alabel, fs, msgperc), 
                       collapse = "\n")
      grid.text(frame1)
      # print obstacle animation
      fo <- framevector2[i]
      frame2 <- paste0(c(" ", fo),
                       collapse = "\n")
      grid.text(frame2)
      Sys.sleep(ssint)
    }
    c = c + 1
  }
}
